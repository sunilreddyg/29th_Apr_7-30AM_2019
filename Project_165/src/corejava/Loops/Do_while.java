package corejava.Loops;

public class Do_while 
{

	public static void main(String[] args)
	{
		
		/*
		 * Dowhile:-->
		 * 		do while is similar to while loop only diff is it accept
		 * 		iteration once eventhow condition was false.
		 */
		
		
		
		int i=0;
		do {
			System.out.println(i);
			i=i+1;
		} while (i<20);
		
		
		
		

	}

}
