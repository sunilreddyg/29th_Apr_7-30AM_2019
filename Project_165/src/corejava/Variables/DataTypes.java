package corejava.Variables;

public class DataTypes {

	public static void main(String[] args) 
	{
		
		/*
		 * int:-->
		 * 		=> Accept only numbers to store
		 * 		=> And default value of integer is 0
		 */
		int a=100;
		int b=200;
		
		int c=a+b;
		System.out.println("total value is => "+c);
		
	
		
		/*
		 * boolean:-->
		 * 		=> Accept only true/false values to store
		 * 		=> And default value is  false.
		 */
		boolean flag=true;
		System.out.println("Boolean status is => "+flag);
		
		
		/*
		 * double:-->
		 * 		=> Accept only decimal values to store.
		 * 		=> And default value is  0
		 */
		double value=1200.001;
		System.out.println("Double value is => "+value);
		
		
		/*
		 * String:-->
		 * 		=> String is a non-primitive datatype 
		 * 		=> It accept set of charactes to store.
		 * 		=> Default value of string is null.
		 * 
		 * 	Note:--> String is not only a datatype and also
		 * 			 called as CLASS.
		 */
		
		String name="Mindq-dsnr";
		String email="info.dsnr@mindqsystems.com";
		String mobile="9030248855";
		
		
		System.out.println(name+"  "+email+"   "+mobile);
		
	
		
		
		
		
	}

}
